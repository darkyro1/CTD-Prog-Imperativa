let texto = "Oi eu sou o vinicius  "

//lenght - conta quantidade de caracteres
console.log(texto.length);

//indexOf - mostra a posição que é pesquisada
console.log(texto.indexOf("sou"));

//slice - mostra o que esta na string de 1 valor ao outro
console.log(texto.slice(0,3));

//trim - remove os espaços do inicio e do fim
console.log(texto.trim());

//split - transfroma o texto em uma array usando " "
console.log(texto.split(" "));

//replace - Troca palavra dentro de uma string
console.log(texto.replace("o vinicius","Heberth"));